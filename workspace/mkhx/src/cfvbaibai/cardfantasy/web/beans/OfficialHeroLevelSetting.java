package cfvbaibai.cardfantasy.web.beans;

public class OfficialHeroLevelSetting {
    private int level;
    private int maxHP;
    private int maxCost;
    public int getLevel() {
        return level;
    }
    public void setLevel(int level) {
        this.level = level;
    }
    public int getMaxHP() {
        return maxHP;
    }
    public void setMaxHP(int maxHP) {
        this.maxHP = maxHP;
    }
    public int getMaxCost() {
        return maxCost;
    }
    public void setMaxCost(int maxCost) {
        this.maxCost = maxCost;
    }
}
