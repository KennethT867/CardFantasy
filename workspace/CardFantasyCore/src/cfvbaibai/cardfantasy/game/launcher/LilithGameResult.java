package cfvbaibai.cardfantasy.game.launcher;

public class LilithGameResult {
    private double avgBattleCount;
    private double avgDamageToLilith;
    private double cvBattleCount;
    private double cvDamageToLilith;
    private String validationResult;

    public double getAvgBattleCount() {
        return avgBattleCount;
    }

    public void setAvgBattleCount(double avgBattleCount) {
        this.avgBattleCount = avgBattleCount;
    }

    public double getAvgDamageToLilith() {
        return avgDamageToLilith;
    }

    public void setAvgDamageToLilith(double avgDamageToLilith) {
        this.avgDamageToLilith = avgDamageToLilith;
    }

    public double getCvBattleCount() {
        return cvBattleCount;
    }

    public void setCvBattleCount(double cvBattleCount) {
        this.cvBattleCount = cvBattleCount;
    }

    public double getCvDamageToLilith() {
        return cvDamageToLilith;
    }

    public void setCvDamageToLilith(double cvDamageToLilith) {
        this.cvDamageToLilith = cvDamageToLilith;
    }

    public String getValidationResult() {
        return validationResult;
    }

    public void setValidationResult(String validationResult) {
        this.validationResult = validationResult;
    }
    
}
